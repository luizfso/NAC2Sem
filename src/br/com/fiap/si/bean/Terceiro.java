package br.com.fiap.si.bean;

public class Terceiro {
	private Integer id;
	private Integer idConta;
	private Integer idContaTerceiro;
	
	public Integer getId() {
		return id;
	}
	
	public void setId(Integer id) {
		this.id = id;
	}

	public Integer getIdConta() {
		return idConta;
	}

	public void setIdConta(Integer idConta) {
		this.idConta = idConta;
	}

	public Integer getIdContaTerceiro() {
		return idContaTerceiro;
	}

	public void setIdContaTerceiro(Integer idContaTerceiro) {
		this.idContaTerceiro = idContaTerceiro;
	}
}
